package model;

/**
 *
 * @
 */
public class Senha {
    
    private int id;
    private String senha;

    public Senha(int id, String senha) {
        this.id = id;
        this.senha = senha;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
    
    
    
}
